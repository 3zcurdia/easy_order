# frozen_string_literal: true

class ApplicationPolicy
  attr_reader :user, :record

  def initialize(user, record)
    @user = user
    @record = record
  end

  def index?
    false
  end

  def show?
    false
  end

  def create?
    false
  end

  def new?
    create?
  end

  def update?
    false
  end

  def edit?
    update?
  end

  def destroy?
    false
  end

  def owner?
    record.user == user
  end

  delegate :admin?, :merchant?, to: :user

  def admin_or_owner?
    admin? || owner?
  end

  def admin_or_merchant?
    admin? || merchant?
  end

  class Scope
    attr_reader :user, :scope

    def initialize(user, scope)
      @user = user
      @scope = scope
    end

    def resolve
      scope.all
    end

    delegate :admin?, to: :user
  end
end
